def grade(score):
    if score >= 80:
        return "A1"
    elif score >= 70:
        return "B2"
    elif score >= 60:
        return "B3"
    elif score >= 55:
        return "C4"
    elif score >= 50:
        return "C5"
    elif score >= 45:
        return "C6"
    elif score >= 40:
        return "D7"
    elif score >= 35:
        return "E8"
    else:
        return "F9"
